# The usage of function "range()"

for i in range(5):
    print(f'{i:<3}*')
print()

# create a list with range from 3 to 7 (8 not included)
nums_1 = list(range(3,8))
print(nums_1)
print()

# create a list with range from 1 to 9 with a gap of 2 
nums_2 = list(range(1,10,2))
print(nums_2)
print()

# create a list with range from 10 to 2 with a gap of -2
nums_3 = list(range(10,1,-2))
print(nums_3)
print()




